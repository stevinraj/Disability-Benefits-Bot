    [LuisModel("3fefaa87-3940-403c-912d-3816786c7f7b", "8bb97845d56f44839970746424870001")]
    [Serializable]
    class GetBenefitsDialog : LuisDialog<GetBenefits>
    {
        private readonly BuildFormDelegate<GetBenefits> BenefitsForm;

        internal GetBenefitsDialog(BuildFormDelegate<GetBenefits> BenefitsForm)
        {
            this.BenefitsForm = BenefitsForm;
        }

        [LuisIntent("")]
        public async Task None(IDialogContext context, LuisResult result)
        {
            await context.PostAsync("I'm sorry. I didn't understand you.");
            context.Wait(MessageReceived);
        }

       [LuisIntent("List")]
        public async Task ProcessBenefitsForm(IDialogContext context, LuisResult result)
        {
            var benefitsForm = new FormDialog<GetBenefits>(new GetBenefits(), this.BenefitsForm, FormOptions.PromptInStart);
            context.Call<GetBenefits>(benefitsForm, BenefitsFormComplete);
        }

        private async Task BenefitsFormComplete(IDialogContext context, IAwaitable<GetBenefits> result)
        {
            GetBenefits order = null;
            try
            {
                order = await result;
            }
            catch (OperationCanceledException)
            {
                await context.PostAsync("You canceled the form!");
                return;
            }

            if (order != null)
            {
              
                var TyD = (int)order.Kind; var age = order.Age;
              
                int i; int MaxRows;

                MaxRows = ArrayApplication.MyArray.AgeList1.GetLength(0);

                for (i = 0; i <= MaxRows; i++)
                {
                    if (ArrayApplication.MyArray.ToD[i, TyD] + ArrayApplication.MyArray.AgeList1[i, age] == 2)
                    {
                        await context.PostAsync(i + 1 + ". " + ArrayApplication.MyArray.BenfNames[i]);
                    }
                    
                }
                context.Wait(MessageReceived);
            }
        }

    }
